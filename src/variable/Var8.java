package variable;

public class Var8 {

	public static void main(String[] args) {
		//정수
		byte b = 127; // -128 ~ 127
		short s = 32767; // -32768 ~ 32767
		int i = 2137483647; // -21억 ~ 21억 (약 20억)	정수 리터럴

		long l = 9223372036854775807L;	//뒤에 L을 붙여야함

		//실수
		float f = 10.0f;	// 권장하지 않음
		double d = 10.0;	// 실수 리터럴



	}

}
